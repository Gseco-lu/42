#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (*(str + i) != '\0')
	{
		if (((*(str + i) >= 48) && (*(str + i) <= 57)))
		{
			count++;
		}
		i++;
	}
	if (count == i)
	{
		return (1);
	}
	return (0);
}
